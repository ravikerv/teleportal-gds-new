/**
 * Generate src/preview/nhsuk-scoped.generated.css from nhsuk-frontend.
 *
 * nhsuk-frontend styles bare elements (html, body, h1-h6, a, ol, …) which
 * bleeds into the builder chrome when imported globally. This script
 * prefixes every selector with `.nhsuk-scope` (html/body/:root become the
 * scope itself) so NHS styles only apply inside preview surfaces that opt
 * in. Runs automatically via predev/prebuild.
 */

import { createRequire } from 'node:module';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const postcss = require('postcss');

const here = dirname(fileURLToPath(import.meta.url));
const source = require.resolve('nhsuk-frontend/dist/nhsuk/nhsuk-frontend.css');
const target = join(here, '../src/preview/nhsuk-scoped.generated.css');

const css = readFileSync(source, 'utf8');
const root = postcss.parse(css);

root.walkRules((rule) => {
  if (rule.parent?.type === 'atrule' && /keyframes/i.test(rule.parent.name ?? '')) {
    return;
  }
  rule.selectors = rule.selectors.map((sel) => {
    const trimmed = sel.trim();
    if (/^(html|:root)\b/.test(trimmed)) {
      return trimmed.replace(/^(html|:root)/, '.nhsuk-scope');
    }
    if (/^body\b/.test(trimmed)) {
      return trimmed.replace(/^body/, '.nhsuk-scope');
    }
    return `.nhsuk-scope ${trimmed}`;
  });
});

mkdirSync(dirname(target), { recursive: true });
writeFileSync(
  target,
  `/* GENERATED by scripts/scope-nhsuk-css.mjs — do not edit. */\n${root.toString()}`,
);
console.log(`scoped nhsuk-frontend css → ${target}`);
